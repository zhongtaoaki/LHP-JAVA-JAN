package firstWeek;
/**
 * java有8种基本数据类型 首字母小写
 * 非基本数据类型（除了8种基本数据类型） 引用数据类型 参照型
 * 引用数据类型 首字母大写 
 * String 字符串 文字列
 */

public class StringDemo {
    public static void main(String[] args) {

        String s = "Hello World";

        System.out.println(s);
        System.out.println("这个字符串的\"长度\"是： " + s.length());

        System.out.println("1" + "0");
        System.out.println(1 + 0);

    }
}
